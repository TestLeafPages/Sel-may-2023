package week1.day1;

public class LearnSwitchCase {

	public static void main(String[] args) {

		String browser ="Edge";
		
		
		switch(browser) {
		
		case "Edge": {
			System.out.println("Lauch the Edge browser");
			break;
		}

		case "Chrome": {
			System.out.println("Lauch the Chrome browser");
			break;
		}

		case "Firefox": {
			System.out.println("Lauch the firefox browser");
			break;
		}

		case "Safari": {
			System.out.println("Lauch the Safari browser");
			break;
		}
		default:
		{
			System.out.println("Check your input");
		}
		
		
		}
		
		
		
		
		
		
		
		
	}

}
