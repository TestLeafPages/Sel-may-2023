package week1.day1;

public class LearnOperators {

	public static void main(String[] args) {
		int i=10;
		int j=5;
		
		System.out.println(i+j);
		System.out.println(i-j);
		System.out.println(i/j);//quotient
		System.out.println(i%j);
		//% -->print the remainder
		
		System.out.println(++i);
		System.out.println(i);
		

	}

}
